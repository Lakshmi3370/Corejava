class Vowelssmall {
	public static void main(String[] args) {
		char ch = 'u';
		if(ch=='a'){
			System.out.println("a is a vowel");
		}
		else if(ch=='e'){
			System.out.println("e is a vowel");
		}
		else if(ch=='i'){
			System.out.println("i is a vowel");
		}
		else if(ch=='O'){
			System.out.println("o is a vowel");
		}
		else if(ch=='u'){
			System.out.println("u is a vowel");
		}
		
		}
}